    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(obje.guarActi(Integer.parseInt(this.txtCodi.getText()), this.txtNomb.getText()))
        {
            JOptionPane.showMessageDialog(this, "Ha guardado");
            this.txtCodi.setText("");
            this.txtNomb.setText("");
            this.cargarCombos();
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Error");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
        Actividad seleccionado = (Actividad)this.cmbActi.getSelectedItem();
        JOptionPane.showMessageDialog(this, 
                String.format("El código es: %s, el nombre es: %s", seleccionado.getCodi(), seleccionado.getNomb()));
    }//GEN-LAST:event_btnProcesarActionPerformed

