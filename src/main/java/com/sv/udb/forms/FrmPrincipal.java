    private void cargarCombos()
    {
        DefaultComboBoxModel<Actividad> modeActi = new DefaultComboBoxModel<>();
        for (Actividad temp : obje.consTodo()) {
            modeActi.addElement(temp);
        }
        this.cmbActi.setModel((DefaultComboBoxModel)modeActi);
    }

